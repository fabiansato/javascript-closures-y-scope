// Declarar una variable global
var miVariableGlobal = "Hola Mundo";

// Definir una función que usa la variable global
function mostrarVariableGlobal() {
    console.log(miVariableGlobal); // Accede a la variable global
}

// Mostrar la variable global fuera de la función
console.log(miVariableGlobal); // 'Hola Mundo'

// Ejecutar la función para demostrar el acceso a la variable global
mostrarVariableGlobal(); // 'Hola Mundo's